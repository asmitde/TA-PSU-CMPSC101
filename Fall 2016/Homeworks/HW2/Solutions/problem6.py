# Description: Program to convert celsius to fahrenheit

# Prompt the user to enter a value in celsius
celsius = float(input('Enter temperature in degree celsius: '))

# Convert celsius to fahrenheit
fahrenheit = (9 / 5) * celsius + 32

# Display the converted value
print('Temperature in degree fahrenheit:', fahrenheit)
