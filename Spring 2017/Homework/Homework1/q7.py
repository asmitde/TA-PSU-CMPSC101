# Input temp(c) from user
t_celsius = float(input("Enter temperature in degree celsius: "))

# Calculate temp(f) using formula
t_fahrenheit = 9 / 5 * t_celsius + 32

# Output temp(f)
print(t_celsius, "C is equivalent to", t_fahrenheit, "F")
